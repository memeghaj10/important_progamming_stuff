	int eggDrop(int n, int k) 
{
    int dp[n+1][k+1];
   
   memset(dp,-1,sizeof(dp));
    
    for(int j=0;j<k+1;j++) {
        dp[0][j]=0;
    }
    
    for(int i=1;i<=n;i++) {
        dp[i][1]=1;
        dp[i][0]=0;
    }
    
    for(int j=1;j<=k;j++) {
        dp[1][j]=j;
    }
    
    
    for(int i=2;i<=n;i++) {
        for(int j=2;j<=k;j++) {
            dp[i][j]=INT_MAX;
            for(int x=1;x<=j;x++) {
                int temp=1+max(dp[i-1][x-1],dp[i][j-x]);
                dp[i][j]=min(dp[i][j],temp);
            }
        }
    }
    
   return dp[n][k];
    
}




